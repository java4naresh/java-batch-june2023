class Switch{
public static void main(String args[])
{
     int Vowel=9;
     switch(Vowel) {
     case 1: System.out.println('A');
      break;
     case 2: System.out.println('E');
      break;
     case 3: System.out.println('I');
      break;
     case 4: System.out.println('O');
      break;
     case 5: System.out.println('U');
      break;
     case 6: System.out.println('a');
      break;
     case 7: System.out.println('e');
      break;
     case 8: System.out.println('i');
      break;
     case 9: System.out.println('o');
      break;
     case 10: System.out.println('u');
      break;
     default: System.out.println("Consonant");
      break;
    }
}
}