class IfExample {

public static void main(String[] args) {
// if condition example
char gender = 'C';
if(gender == 'F') {
System.out.println("Your gender is Female");
}

if(gender == 'M') {
System.out.println("Your gender is Male");
}

if(gender != 'M' && gender != 'F') {
System.out.println("Your gender is Transgender");
}

}
}