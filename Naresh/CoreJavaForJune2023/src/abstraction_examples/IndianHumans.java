package abstraction_examples;

public class IndianHumans implements HumanTodos {
	
	public void awake() {
		System.out.println(" 7'o clock");
	}
	
	public void excerisize() {
		System.out.println("In India 10% people of the population will do excersize");
	}
	
	public void breakFast() {
		System.out.println("In India Idly/Dosa will eat as a break fast");
	}
	
	public void work() {
		System.out.println("In India 5% of the population is a software engineers");
	}
	
	public void lunch() {
		System.out.println("In India rice will eat as a lunch");
	}
	
	public void snacks() {
		System.out.println("In India samosa will eat as a snacks");
	}
	
	
	public void dinner() {
		System.out.println("In India rice will eat as a lunch");
	}
	
	public void sleep() {
		System.out.println(" 10'o clock");
	}
	

}
