package abstraction_examples;

public class USCitizenTodos implements HumanTodos {

	public void awake() {
		System.out.println(" 8'o clock");
	}
	
	public void excerisize() {
		System.out.println("In India 50% people of the population will do excersize");
	}
	
	public void breakFast() {
		System.out.println("In US Sandwich/Pizza will eat as a break fast");
	}
	
	public void work() {
		System.out.println("In US 20% of the population is a software engineers");
	}
	
	public void lunch() {
		System.out.println("In US Pizza will eat as a lunch");
	}
	
	public void snacks() {
		System.out.println("In US India Pizza will eat as a snacks");
	}
	
	
	public void dinner() {
		System.out.println("In US Pizza will eat as a lunch");
	}
	
	public void sleep() {
		System.out.println(" 11'o clock");
	}
}
