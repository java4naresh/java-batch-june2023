class IntExample 
{
	public static void main(String[] args) 
	{
		int iphonePrice = 68500;
		int arithOperations = 10+(10*28)/(8*10)-5;//355
		int arithOperations2 = 28%8*10*10;//3*10*10
		int coef = 29/8;//3
		int rem = 29%8;//5

		System.out.println("My Iphone price is "+iphonePrice);
		System.out.println(arithOperations);//300
		System.out.println(arithOperations2);//400
		System.out.println(coef);//3
		System.out.println(rem);//5
	}
}
